// Faire une condition qui permet de vérifier si la personne a + de 18 ans et mesure + de 140, dans ce cas :
// Afficher dans la console : "Je peux rentrer dans le manège"
// Sinon afficher dans la console : "Je n'ai pas le droit de rentrer dans le manège"

